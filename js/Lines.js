function Line_Table(flag){
   if (flag !== undefined){
      document.getElementById('P_value_line').innerHTML = eval("P_" + flag).toFixed(0) + ' kW';
      document.getElementById('Q_value_line').innerHTML = eval("Q_" + flag).toFixed(0) + ' kvar';
      document.getElementById('U_value_line').innerHTML = (parseFloat(eval("U_" + flag))*100).toFixed(2) + ' %';
   }
}
function Line_Click(elementid,elementnum,P,Q,U){
   let id = elementid.toString();
   if(id !== undefined){
      document.getElementById(id).addEventListener('click', function () {
         document.getElementById('U_value_line').innerHTML = (parseFloat(U) * 100).toFixed(2) + ' %';
         document.getElementById('P_value_line').innerHTML = parseFloat(P).toFixed(0) + ' kW';
         document.getElementById('Q_value_line').innerHTML = parseFloat(Q).toFixed(0) + ' kvar';
         document.getElementById('LineID').innerHTML = 'Line ' + elementnum;
         flag2 = id;
      });
   }
}


