function LVTopologyClickListener(loadnum){
    let PVlist = []; // if you have any transformer that connects to the different low voltage topology, use this list to change the LV preview.
    console.log(PVlist.indexOf(loadnum));
    if (PVlist.indexOf(loadnum) === -1){
        LVTopologyShow(0);
    }else {
        LVTopologyShow(1);
    }
}

function LVTopologyShow(elementid){
    if(elementid === 0){
        document.getElementById('LV_SVG').style.visibility = 'hidden';
        document.getElementById('LV_SVG2').style.visibility = 'visible';

    }else if(elementid === 1){
        document.getElementById('LV_SVG2').style.visibility = 'hidden';
        document.getElementById('LV_SVG').style.visibility = 'visible';
    }
    document.getElementById('Clear_Button').style.visibility = 'visible';
    document.getElementById('ButtonText').innerHTML = 'Zoom In';
}